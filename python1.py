# ==================== ЗАДАЧА №10 ====================
# Калькулятор
print("\nЗадача 10: Калькулятор. Вводите команды в формате: 'число операция число' (например: '5 + 3')")
print("Операции: +, -, *, /, %, !, x (x - завершение)")
print("Для факториала (!): 'число !'")
print("Для выхода: 'число x'")

while True:
    try:
        cmd = input().split()
        if not cmd:
            continue
        
        a = int(cmd[0])
        if cmd[1] == "x":
            print(a)
            break
        elif cmd[1] == "!":
            if a < 0:
                continue
            fact = 1
            for i in range(2, a + 1):
                fact *= i
            print(fact)
        else:
            b = int(cmd[2])
            op = cmd[1]
            if op == "+":
                print(a + b)
            elif op == "-":
                print(a - b)
            elif op == "*":
                print(a * b)
            elif op == "/":
                if b == 0:
                    continue
                print(a // b)
            elif op == "%":
                if b == 0:
                    continue
                print(a % b)
    except (ValueError, IndexError):
        continue

