# меньше 150 см.
print("Задание 8")
heights = []
while True:
    x = input()
    if x == "!":
        break
    h = int(x)
    if 150 <= h <= 190:
        heights.append(h)

print(len(heights))
if heights:
    print(min(heights), max(heights))

