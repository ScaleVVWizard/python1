# ==================== ЗАДАЧА №10 ====================
# Задание 10 Напишите калькулятор для целочисленных вычислений, который обрабатывает серию 
# команд и выводит результат вычислений по каждой из них.
print("Задание 10")
def fact(n):
    if n < 0:
        return None
    r = 1
    for i in range(2, n + 1):
        r *= i
    return r

while True:
    try:
        a = int(input())
        op = input()
        if op == "x":
            print(a)
            break
        elif op == "!":
            f = fact(a)
            if f is not None:
                print(f)
        else:
            b = int(input())
            if op == "+":
                print(a + b)
            elif op == "-":
                print(a - b)
            elif op == "*":
                print(a * b)
            elif op == "/" and b != 0:
                print(a // b)
            elif op == "%" and b != 0:
                print(a % b)
    except:
        break

