b = (n // 10) % 10
c = n % 10
mn = min(a, b, c)
mx = max(a, b, c)
mid = a + b + c - mn - mx
if (mn + mx) / 2 == mid:
    print("Вы ввели красивое число")
else:
    print("Жаль, вы ввели обычное число")

